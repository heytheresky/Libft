int	ft_atoi(const char *str)
{
	int	i;
	int	num;
	int	sin;

	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
	{
		i++;
	}
	sin = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sin *= -1;
		i++;
	}
	num = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	return (num * sin);
}
